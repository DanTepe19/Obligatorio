    private void login(){
        String usu = txtUsuario.getText();
        String password = new String(txtPassword.getPassword());
        
        Object usuario = llamarLogin(usu, password);
        if(usuario == null){
            jLabel5.setText("Credenciales incorrectas");
        } else {
            proximoCU(usuario);
            txtUsuario.setText("");
            txtPassword.setText("");
            jLabel5.setText("");
        }
    }


